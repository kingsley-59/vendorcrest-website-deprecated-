              <p>
                Do you know why your social media feed is the way it is; why the
                posts that you see happen to be there just then; and why it
                seems like people almost never see what you post (or it's the
                reverse)?
              </p>

              <p>
                There is something called Algorithm. It isn't much of a secret,
                but not everyone understands why it is needed, even as it
                affects them and their social media activities. Read on and
                you'll understand better why it is what it is. The Algorithm is
                some sort of filter that is specific to each social media
                platforms. It makes it easier to surf your favorite social media
                platforms by providing you with posts from the accounts that it
                "thinks" is more relevant to you.
              </p>

              <p>
                The algorithm distinguishes relevant posts by focusing on
                specific details.
              </p>

              <p>
                1. The traction it pulls from other users, and how they interact
                with it. This would include likes, comments, shares, saves and
                even downloads. The more this happens, the more frequent you're
                bound to see posts from some accounts.
              </p>

              <p>
                2. The settings you allow on your social media platforms. Due to
                privacy and data protection, every user has the right to change
                their feed preference —even though the option is limited still.
              </p>

              <blockquote>
                <p>
                  You may think the algorithm is askew and it's not providing
                  you with all of the relevant posts (against its purpose), but
                  a closer view of certain factors would help you realise
                  there's not much of a secret to be unravelled. Rather, a
                  system to be understood.
                </p>
              </blockquote>

              <p>
                At first, posts were based majorly on timing. Only those that
                post when you are online would be able to reach you. This proved
                uneffective as some users were recorded to follow hundreds to
                thousands of accounts. Then the algorithm was adjusted. Again.
                And again. And again. And again.Now, it's a combination of time,
                relevancy, the accounts you interact with more, and some
                out-of-the-blues suggestions. To be able to notice all of the
                accounts you consider relevant you have to spend more *time for
                them to organically reach you (that is, for posts to surface
                without the *sponsored tag).
              </p>

              <p>
                Recently, in defense to some users complaint about shaddy
                algorithms, Instagram replies: "We have not made any recent
                changes to feed ranking, and we never hide posts from people
                you're following – if you keep scrolling, you will see them all.
                Again, your feed is personalized to you and evolves over time
                based on how you use Instagram.✌️" What does this mean? As much
                as we would love to soot the activities of the algorithm,
                technically, it is subject to us. How? The algorithms involve
                machine learning and data science which make them capable of
                parsing data and ranking posts (specific to you). The shaddy
                part. Because of the impact it has on businesses, some believe
                the algorithm was set up to increase the use of ads since they
                can’t reach their audience organically. This clearly means more
                ROIs for social networks. It's not like it is true, but it looks
                like it. What do you think?
              </p>

